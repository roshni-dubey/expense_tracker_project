expenses = []  # list of expense dictionaries

print("Welcome to Expense Tracker üí∏")

while True:
    print("\n======= MENU =======")
    print("1Ô∏è‚É£ Add Expense")
    print("2Ô∏è‚É£ View All Expenses")
    print("3Ô∏è‚É£ View Total Spending")
    print("4Ô∏è‚É£ View Spending by Category")
    print("5Ô∏è‚É£ Exit")
    print("=====================")

    choice = input("Enter your choice (1-5): ")

    # 1Ô∏è‚É£ Add Expense
    if choice == "1":
        date = input("Enter date (DD-MM-YYYY): ")
        category = input("Enter category (Food, Travel, Shopping, etc): ")
        description = input("Enter short description: ")
        amount = float(input("Enter amount (‚Çπ): "))

        expense = {
            "date": date,
            "category": category,
            "description": description,
            "amount": amount
        }

        expenses.append(expense)
        print("\n‚úÖ Expense added successfully!")

    # 2Ô∏è‚É£ View All Expenses
    elif choice == "2":
        if len(expenses) == 0:
            print("\n‚ö†Ô∏è No expenses recorded yet.")
        else:
            print("\n--- All Expenses ---")
            i = 1
            for e in expenses:
                print(f"{i}. {e['date']} | {e['category']} | {e['description']} | ‚Çπ{e['amount']}")
                i += 1
            print("---------------------")

    # 3Ô∏è‚É£ View Total Spending
    elif choice == "3":
        total = 0
        for e in expenses:
            total += e["amount"]
        print(f"\nüí∞ Total Spending = ‚Çπ{total}")

    # 4Ô∏è‚É£ Spending by Category
    elif choice == "4":
        if len(expenses) == 0:
            print("\n‚ö†Ô∏è No expenses recorded yet.")
        else:
            summary = {}
            for e in expenses:
                cat = e["category"]
                if cat in summary:
                    summary[cat] += e["amount"]
                else:
                    summary[cat] = e["amount"]

            print("\nüìä Spending by Category:")
            for cat, amt in summary.items():
                print(f"{cat}: ‚Çπ{amt}")

    # 5Ô∏è‚É£ Exit
    elif choice == "5":
        print("\nüëã Thanks for using Expense Tracker! Bye!")
        break

    else:
        print("\n‚ùå Invalid choice. Please try again.")
